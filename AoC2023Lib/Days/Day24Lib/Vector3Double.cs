namespace AoC2023Lib.Days.Day24Lib;

internal class Vector3Double
{
    public Vector3Double()
    {
    }

    public Vector3Double(double x, double y, double z)
    {
        X = x;
        Y = y;
        Z = z;
    }

    public double X { get; set; }
    public double Y { get; set; }
    public double Z { get; set; }
}
